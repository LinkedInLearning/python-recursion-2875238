# No base case, no movement towards base case.
count = 1 
while True:   
  print(count)

# We have a base case, but no movement towards it.
count = 1 
while True: 
  if count >10 : 
    break 
  print(count)
 
# Base Case and movement towards it.
count = 1 
while True: 
  if count >10 : 
    break 
  print(count)
  count += 1
